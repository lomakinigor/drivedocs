//
// Оператор сейчас — физическое лицо (юрлицо/ИП пока не зарегистрировано).
// 152-ФЗ прямо допускает физлицо как оператора ПД (ст. 3) — обязанность
// иметь политику/согласие не зависит от наличия юрлица или монетизации,
// она возникает от самого факта обработки ПД пользователей.
//
// ВАЖНО: ФИО и e-mail ниже — плейсхолдеры до момента, пока владелец их не
// пришлёт. Если позже появится ИП/ООО — сменить legalForm и заполнить
// inn/ogrn/address, остальной текст пересчитается сам. Структура и общие
// формулировки — стандартные для российских сервисов (152-ФЗ, ГК РФ гл. 39,
// ст. 437 — публичная оферта), но это не заменяет юридическую экспертизу.

export interface LegalSection {
  heading: string
  paragraphs: string[]
}

export interface LegalDocument {
  slug: 'offer' | 'privacy' | 'consent'
  title: string
  updatedAt: string
  sections: LegalSection[]
}

export type OperatorLegalForm = 'individual' | 'ip' | 'ooo'

export interface OperatorProfile {
  name: string // ФИО (физлицо/ИП) или название организации (ООО)
  legalForm: OperatorLegalForm
  inn: string | null
  ogrn: string | null // ОГРН (ООО) или ОГРНИП (ИП) — не применимо для физлица
  address: string | null
  email: string
  phone: string | null
}

