function ogrnLabel(): string {
  return OPERATOR.legalForm === 'ooo' ? 'ОГРН' : 'ОГРНИП'
}

/** Краткое именование оператора для упоминания в тексте документов. */
function operatorMention(): string {
  if (OPERATOR.legalForm === 'individual') {
    return `${OPERATOR.name} (физическое лицо)`
  }
  const parts = [OPERATOR.name]
  if (OPERATOR.inn) parts.push(`ИНН ${OPERATOR.inn}`)
  if (OPERATOR.ogrn) parts.push(`${ogrnLabel()} ${OPERATOR.ogrn}`)
  return parts.join(', ')
}

/** Блок «Реквизиты оператора» — только реально заполненные строки. */
function operatorDetailsLines(): string[] {
  const lines = [OPERATOR.name]
  if (OPERATOR.legalForm === 'individual') lines.push('Статус: физическое лицо')
  if (OPERATOR.inn) lines.push('ИНН: ' + OPERATOR.inn)
  if (OPERATOR.ogrn) lines.push(`${ogrnLabel()}: ${OPERATOR.ogrn}`)
  if (OPERATOR.address) lines.push('Адрес: ' + OPERATOR.address)
  lines.push('E-mail: ' + OPERATOR.email)
  if (OPERATOR.phone) lines.push('Телефон: ' + OPERATOR.phone)
  return lines
}

