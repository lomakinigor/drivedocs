// ─── Onboarding & settings help content ──────────────────────────────────────
// Справочный контент, показываемый в OnboardingWizard и SettingsPage.
// Стиль: plain-language, без юридического жаргона. Структура наследует
// пример из documentHelp.ts — короткие секции с заголовком и текстом.
// Feature: F-021 · Task: T-126 · Decision: D-022

export interface HelpSection {
  /** Подзаголовок секции */
  heading: string
  /** Основной текст параграфом (опционально) */
  body?: string
  /** Маркированный список (опционально) */
  list?: string[]
  /** Тон акцента: нейтральный (по умолчанию) или предупреждение */
  tone?: 'info' | 'warning'
}

export interface HelpContent {
  /** Заголовок bottom sheet */
  title: string
  /** Короткий лид (1 предложение под заголовком) */
  lead?: string
  /** Секции */
  sections: HelpSection[]
  /** Финальная подсказка / disclaimer (опционально) */
  footnote?: string
}

